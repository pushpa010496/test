                                                    materials and processes, with the goal of ensuring final product
                                                    quality and performance. Compared with other spectroscopic
                                                    technologies such as Near-Infrared (NIR), Mid-Infrared (MIR) and
                                                    UV-Vis, Raman spectroscopy is demonstrating clear advantages from a
