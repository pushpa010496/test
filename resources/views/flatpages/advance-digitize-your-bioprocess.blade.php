                                            </div>
                                            <div class='col-lg-7 col-md-7 col-sm-12 '>

                                                <p style="color: #666666;font-size: 15px;line-height: 28px;">
                                                    Process Analytical Technology (PAT) is a regulatory framework
                                                    initiated by the United States’ Food and Drug Administration (FDA)
                                                    that encourages innovation in pharmaceutical development,
                                                    manufacturing, and quality assurance. The goal of PAT is to build
                                                    quality into biopharmaceutical production processes by monitoring
                                                    and controlling the process in real time. Through the identification
                                                    of critical process parameters (CPP) and critical quality attributes
                                                    (CQA) and maintenance of CPPs and CQAs within a specified design
                                                    space, PAT facilitates a dynamic manufacturing process that can
                                                    compensate for variability in raw materials and equipment. Quality
                                                    is therefore built into the process by Design (QbD), rather than
                                                    being tested into the products.</p>


